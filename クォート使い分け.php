<?php
/*
 このファイルは
 ・シングルクォーテーション ' '
 ・ダブルクォーテーション " "
 の違いを、コードと実行結果だけで理解するためのもの。

 重要な違い：
 1) 変数が展開されるか
 2) \n が改行として解釈されるか
 3) 配列や式が評価されるか
*/

// ===== 準備 =====
$name  = "山田";
$price = 1200;
$tax   = 0.1;
$user  = ['name' => '佐藤'];

// =====================================================
// シングルクォーテーション
// =====================================================
/*
 ' ' の特徴：
 ・中身は一切解析されない
 ・変数は変数として扱われない
 ・\n は改行にならない
 ・配列や計算式も評価されない
 → すべて「ただの文字」として出力される
*/
echo "===== シングルクォーテーション =====\n";

echo '名前：$name' . "\n";                 // $name は文字のまま
echo '改行テスト：1行目\n2行目' . "\n";   // \n は文字のまま
echo '計算：$price + ($price * $tax)' . "\n"; // 計算されない
echo '配列：{$user["name"]}' . "\n";       // 配列は展開されない

echo "\n";

// =====================================================
// ダブルクォーテーション
// =====================================================
/*
 " " の特徴：
 ・中身を解析する
 ・変数は値に置き換えられる
 ・\n は改行として解釈される
 ・配列は {} を使えば展開できる
 ・計算は文字列の外で評価する必要がある
*/
echo "===== ダブルクォーテーション =====\n";

echo "名前：$name\n";               // 変数が展開される
echo "改行テスト：1行目\n2行目\n"; // \n が改行になる

// 計算は先に評価してから文字列に連結する
echo "計算：" . ($price + ($price * $tax)) . "\n";

// 配列は {} を使って展開する
echo "配列：{$user['name']}\n";

